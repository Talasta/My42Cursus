#include "fdf.h"

t_mlx	auto_rot(t_mlx mlx)
{
	mlx.cnf.rot = (mlx.cnf.rot + 1) % 5;
	if (mlx.cnf.rot % 2 != 0 || mlx.cnf.rot == 0)
	{
		mlx.cnf.a_x = 0;
		mlx.cnf.a_y = 0;
	}
	return (mlx);
}
